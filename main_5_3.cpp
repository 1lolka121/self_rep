#include <iostream>
using namespace std;

int main() {
    double A, B, C;

    // Ввод длин палочек
    cout << "Введите длины трех палочек: ";
    cin >> A >> B >> C;

    // Проверка условия существования треугольника с использованием логических операторов
    if (A + B > C && A + C > B && B + C > A) {
        cout << "Из этих палочек можно сложить треугольник." << endl;
    } else {
        cout << "Из этих палочек нельзя сложить треугольник." << endl;
    }

    return 0;
}



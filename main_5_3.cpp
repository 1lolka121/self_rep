#include <iostream>
using namespace std;

int main() {
    int a, b, c;

    // Ввод трех чисел
    cout << "Введите три числа: ";
    cin >> a >> b >> c;

    // Проверка совпадений с использованием логических операторов
    if (a == b && b == c) {
        cout << "3 совпадающих числа." << endl;
    } else if (a == b || a == c || b == c) {
        cout << "2 совпадающих числа." << endl;
    } else {
        cout << "0 совпадающих чисел." << endl;
    }

    return 0;
}
